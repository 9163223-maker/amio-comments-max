'use strict';
const assert = require('assert');
const contractService = require('../services/runtimeContractService');
const startupLog = require('../services/startupLogService');
const routes = require('../runtime-contract-routes');
const bootstrap = require('../pr180-startup-log-bootstrap');
const contract = contractService.buildContract();
assert.strictEqual(contract.runtime, 'RUNTIME-CONTRACT-PR196');
assert.strictEqual(contract.safe, true);
assert.strictEqual(contract.startupPath.ok, true);
assert.strictEqual(contract.routes.channelsList.ok, true);
assert.strictEqual(contract.routes.channelsList.usesSharedPicker, true);
assert.strictEqual(contract.routes.buttonsChannelPicker.ok, true);
assert.strictEqual(contract.routes.buttonsChannelPicker.usesSharedPicker, true);
assert.strictEqual(contract.dataProviders.cc5GetPostsAvailable, true);
assert.strictEqual(contract.dataProviders.akPostsHasAdminChannelPostKey, true);
assert.strictEqual(contract.routes.buttonsPostPicker.dbBacked, false);
assert.strictEqual(contract.routes.buttonsPostPicker.stillStoreBacked, true);
assert.strictEqual(contract.routes.buttonsPostPicker.ok, false);
assert.strictEqual(contract.contractLiveOk, false);
assert(contract.mismatches.includes('buttons_post_picker_not_db_backed'));
assert(contract.mismatches.includes('buttons_post_picker_still_store_backed'));
const sanitized = startupLog.sanitizeEntry({ runtimeVersion: 'x', sourceMarker: 'y', runtimeContract: contract });
assert.strictEqual(sanitized.runtimeContract.contractLiveOk, false);
assert.strictEqual(sanitized.runtimeContract.routes.buttonsPostPicker.expectedPostsProvider, 'cc5-db-core.getPosts');
assert(sanitized.runtimeContract.mismatches.includes('buttons_post_picker_still_store_backed'));
const runtimeInfo = bootstrap.runtimeInfo();
assert.strictEqual(runtimeInfo.runtimeContract.runtime, 'RUNTIME-CONTRACT-PR196');
const payload = routes.payload();
assert.strictEqual(payload.ok, false);
assert.strictEqual(payload.contract.runtime, 'RUNTIME-CONTRACT-PR196');
console.log('PR196 runtime contract log assertions passed');
